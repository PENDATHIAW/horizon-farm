const json = (res, status, payload) => {
  res.statusCode = status;
  res.setHeader('Content-Type', 'application/json; charset=utf-8');
  res.end(JSON.stringify(payload));
};

const readBody = async (req) => new Promise((resolve, reject) => {
  let body = '';
  req.on('data', (chunk) => { body += chunk; if (body.length > 1_000_000) reject(new Error('Payload trop volumineux')); });
  req.on('end', () => {
    try { resolve(body ? JSON.parse(body) : {}); } catch { reject(new Error('JSON invalide')); }
  });
  req.on('error', reject);
});

const env = (key) => process.env[key] || '';
const safePath = (path = '') => {
  if (!path || path.includes('..') || path.startsWith('/') || path.includes('node_modules')) return false;
  if (path.startsWith('.github/')) return false;
  if (/\.env/i.test(path)) return false;
  return ['src/', 'api/', 'docs/'].some((prefix) => path.startsWith(prefix));
};

const gh = async (url, options = {}) => {
  const token = env('GITHUB_TOKEN');
  const response = await fetch(url, {
    ...options,
    headers: {
      Accept: 'application/vnd.github+json',
      Authorization: `Bearer ${token}`,
      'X-GitHub-Api-Version': '2022-11-28',
      ...(options.headers || {}),
    },
  });
  const text = await response.text();
  let data = null;
  try { data = text ? JSON.parse(text) : null; } catch { data = { raw: text }; }
  if (!response.ok) {
    const error = new Error(data?.message || `GitHub API ${response.status}`);
    error.status = response.status;
    error.data = data;
    throw error;
  }
  return data;
};

const contentUrl = ({ owner, repo, path, branch }) => `https://api.github.com/repos/${owner}/${repo}/contents/${encodeURIComponent(path).replace(/%2F/g, '/')}?ref=${encodeURIComponent(branch)}`;

const getFileSha = async ({ owner, repo, path, branch }) => {
  try {
    const data = await gh(contentUrl({ owner, repo, path, branch }));
    return data?.sha || null;
  } catch (error) {
    if (error.status === 404) return null;
    throw error;
  }
};

const getFileContent = async ({ owner, repo, path, branch }) => {
  const data = await gh(contentUrl({ owner, repo, path, branch }));
  return Buffer.from(data.content || '', 'base64').toString('utf8');
};

const upsertFile = async ({ owner, repo, branch, path, content, message }) => {
  const sha = await getFileSha({ owner, repo, path, branch });
  const payload = { message, content: Buffer.from(content, 'utf8').toString('base64'), branch, ...(sha ? { sha } : {}) };
  const data = await gh(`https://api.github.com/repos/${owner}/${repo}/contents/${encodeURIComponent(path).replace(/%2F/g, '/')}`, {
    method: 'PUT', headers: { 'Content-Type': 'application/json' }, body: JSON.stringify(payload),
  });
  return { path, sha: data?.content?.sha, commit: data?.commit?.sha };
};

const packageReadme = ({ packageId, lot }) => `# ERP Agent correction package\n\nPackage: ${packageId}\nLot: ${lot || 'non renseigné'}\nDate: ${new Date().toISOString()}\n\nThis file is generated by the secured ERP agent to keep a lightweight trace of controlled correction packages.\n\nThe ERP agent only applies whitelisted packages after approval code validation.\n`;

async function buildPackageFiles({ packageId, lot }) {
  if (packageId === 'lot4_automatisations_terrain_v1') {
    return [{
      path: 'docs/erp-agent/lot4-automatisations-terrain.md',
      content: packageReadme({ packageId, lot }) + `\n## Applied behavior\n\n- Alert audit only flags missing alerts when real risks exist.\n- Audit can create base alerts for detected risks.\n- Tasks still require a dedicated operational engine in a later package.\n\n## Retest\n\nAfter Vercel deploy is ready, run Assistant ERP audit again and verify that Lot 4 status is coherent with the actual risks.\n`,
    }];
  }
  if (packageId === 'lot1_bloquants_revenus_v1') {
    return [{
      path: 'docs/erp-agent/lot1-bloquants-revenus.md',
      content: packageReadme({ packageId, lot }) + `\n## Scope\n\nThis package reserves the controlled correction slot for revenue blockers.\nA code-level package must be prepared module by module before automatic application.\n`,
    }];
  }
  return [];
}

export default async function handler(req, res) {
  if (req.method !== 'POST') return json(res, 405, { ok: false, error: 'Méthode non autorisée' });

  const owner = env('GITHUB_OWNER') || 'PENDATHIAW';
  const repo = env('GITHUB_REPO') || 'horizon-farm';
  const branch = env('GITHUB_BRANCH') || 'feature/objectifs-croissance-centre-decisionnel';
  const token = env('GITHUB_TOKEN');
  const approvalSecret = env('ERP_AGENT_APPROVAL_SECRET');
  const deployHookUrl = env('VERCEL_DEPLOY_HOOK_URL');
  const configured = Boolean(token && approvalSecret);

  try {
    const body = await readBody(req);
    const { approvalCode, lot, files = [], message, dryRun = false, mode = 'apply', packageId = '' } = body;

    if (mode === 'status') {
      return json(res, 200, { ok: configured, status: configured ? 'configured' : 'not_configured', owner, repo, branch, deployHookConfigured: Boolean(deployHookUrl), required: ['GITHUB_TOKEN', 'ERP_AGENT_APPROVAL_SECRET'], optional: ['GITHUB_OWNER', 'GITHUB_REPO', 'GITHUB_BRANCH', 'VERCEL_DEPLOY_HOOK_URL'] });
    }

    if (!configured) return json(res, 503, { ok: false, status: 'not_configured', error: 'Agent non configuré : GITHUB_TOKEN et ERP_AGENT_APPROVAL_SECRET sont obligatoires.' });
    if (!approvalCode || approvalCode !== approvalSecret) return json(res, 401, { ok: false, error: 'Code approbation invalide' });

    const commitMessage = message || `ERP agent correction - ${lot || packageId || 'lot contrôlé'}`;
    let finalFiles = Array.isArray(files) ? files : [];
    if (packageId) finalFiles = await buildPackageFiles({ packageId, lot });

    if (dryRun && (!Array.isArray(finalFiles) || finalFiles.length === 0)) {
      return json(res, 200, { ok: true, dryRun: true, status: 'ready', owner, repo, branch, deployHookConfigured: Boolean(deployHookUrl), packageId, message: commitMessage });
    }
    if (!Array.isArray(finalFiles) || finalFiles.length === 0) return json(res, 400, { ok: false, error: packageId ? `Aucun paquet disponible pour ${packageId}` : 'Aucun fichier à corriger fourni par l’agent.' });
    if (finalFiles.length > 12) return json(res, 400, { ok: false, error: 'Trop de fichiers dans un seul lot. Corriger en plus petits lots.' });

    for (const file of finalFiles) {
      if (!safePath(file.path)) return json(res, 400, { ok: false, error: `Chemin interdit : ${file.path}` });
      if (typeof file.content !== 'string') return json(res, 400, { ok: false, error: `Contenu invalide : ${file.path}` });
    }

    if (dryRun) return json(res, 200, { ok: true, dryRun: true, owner, repo, branch, packageId, files: finalFiles.map((f) => f.path), message: commitMessage });

    const results = [];
    for (const file of finalFiles) {
      // GitHub Contents API updates must be serialized to avoid conflicts.
      // eslint-disable-next-line no-await-in-loop
      results.push(await upsertFile({ owner, repo, branch, path: file.path, content: file.content, message: commitMessage }));
    }

    let deploy = null;
    if (deployHookUrl) {
      const deployResponse = await fetch(deployHookUrl, { method: 'POST' });
      deploy = { triggered: deployResponse.ok, status: deployResponse.status };
    }

    return json(res, 200, { ok: true, status: 'committed', owner, repo, branch, lot, packageId, files: results, deploy });
  } catch (error) {
    return json(res, error.status || 500, { ok: false, error: error.message || 'Erreur agent', details: error.data || null });
  }
}
